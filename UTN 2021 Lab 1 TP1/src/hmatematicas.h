#ifndef H_MATEMATICAS_H_INCLUDED
#define H_MATEMATICAS_H_INCLUDED

#include <stdio.h>
#include <stdlib.h>



#endif // H_MATEMATICAS_H_INCLUDED

/** \brief       sumar dos parametros enteros "a" y "b"          sumar 2 int
 *
 * \param a int  parametro entero "a"
 * \param b int  parametro entero "b"
 * \return int   resultado entero "a+b"
 *
 */
int sumar(int a, int b);

/** \brief       restar dos parametros enteros "a" y "b"          restar 2 int
 *
 * \param a int  parametro entero "a"
 * \param b int  parametro entero "b"
 * \return int   resultado entero "a-b"
 *
 */
int restar(int a, int b);
/** \brief       multiplicar dos parametros enteros "a" y "b"          multiplicar 2 int
 *
 * \param a int  parametro entero "a"
 * \param b int  parametro entero "b"
 * \return int   resultado entero "a*b"
 *
 */
int multiplicar(int a, int b);
/** \brief       dividir dos parametros enteros "a" y "b"          dividir 2 float
 *
 * \param a int  dividendo entero "a"
 * \param b int  divisor entero "b"
 * \return float resultado "a/b"
 *
 */
float dividir(int a, int b);
/** \brief                factorial de un entero entre "0" y "12" inclusive          factorial hasta 12
 *
 * \param n int           entero "n"
 * \return long long int  resultado factorial de "n"
 *
 */
long long int factorial(int n);
